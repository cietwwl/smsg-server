	public void setResetForcesTimesLimit(int resetForcesTimesLimit) {
		this.resetForcesTimesLimit = resetForcesTimesLimit;
	}

	public double getForcesCopperAddRatio() {
		return forcesCopperAddRatio;
	}

	public void setForcesCopperAddRatio(double forcesCopperAddRatio) {
		this.forcesCopperAddRatio = forcesCopperAddRatio;
	}

	public int getResetTowerTimesLimit() {
		return resetTowerTimesLimit;
	}

